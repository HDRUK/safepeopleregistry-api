    /**
     * Handle the User "updated" event.
     */
    public function updated(User $user): void
    {
        if ($user->isDirty(['first_name', 'last_name', 'email'])) {

            ActionLog::updateOrCreate(
                ['user_id' => $user->id, 'action' => 'profile_completed'],
                ['updated_at' => now()]
            );
        }
    }

